    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String name = jTextField3.getText().toString();
         try {
            model.setRowCount(0);
            List<Department> findByName = daoDepartment.findByName(name);
            if(findByName.isEmpty()) {
                JOptionPane.showConfirmDialog(this, "Không tìm thấy!!");
            }
            for (Department s : findByName) {
                 Object[] row = new Object[]{
                            s.getId(), s.getName(), s.getDecription()
                        };
                 model.addRow(row);
               
            }
        } catch (RemoteException ex) {
            Logger.getLogger(JFrameDepartment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

