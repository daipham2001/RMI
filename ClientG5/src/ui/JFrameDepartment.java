    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
         String name = jTextField4.getText().toString();
         String decription = jTextArea1.getText().toString();
        try {
            if (daoDepartment.update(new Department(id, name,decription))) {
                load();
                JOptionPane.showMessageDialog(this, "Thành công !");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(JFrameDepartment.class.getName()).log(Level.SEVERE, null, ex);
        }
        id = 0;
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (id != 0) {
            int rep = JOptionPane.showConfirmDialog(this, "Xác nhận xóa phòng ban ?");
            if (rep == 0) {
                try {
                    if (daoDepartment.delete(daoDepartment.findById(id))) {
                        load();
                        JOptionPane.showMessageDialog(this, "Thành công !");
                    } else {
                        JOptionPane.showMessageDialog(this, "Lỗi !");
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(JFrameDepartment.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            id = 0;
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed

